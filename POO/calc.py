class Calc:

    def __init__(self):
        super().__init__()

    def sumar(self, numa, numb):
        print("Suma es igual a " + str(numa+numb))
    
    def restar(self, numa, numb):
        print("Resta es igual a " + str(numa-numb))
    
    def multi(self, numa, numb):
        print("Multiplicacion es igual a " + str(numa*numb))
    
    def divide(self, numa, numb):
        print("Division es igual a " + str(numa/numb))
    